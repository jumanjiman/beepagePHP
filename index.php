	<h2>[ features ]</h2>

<img src="beepage.gif" border="0">

<p>BeepagePHP can dial any standard paging dispatcher,
such as Sprint PCS, Metrocall, Nextel, Cingular, or other common paging
companies.</p>

<img src="how-it-works.png" border="0">

<p>The web interface allows a user to...</p>
<ul>
  <li>Send text messages to any pager and almost any cell phone</li>
  <li>Add subscriber info</li>
  <li>Edit groups of subscribers</li>
  <li>Add service info (e.g., Metrocall dispatcher)</li>
</ul>

<p>When a user composes a page in the web interface, the page
is queued on the server for immediate sending. The server uses a single
modem to dial up each queued service provider (such as Metrocall), then
transmits all pages that have been queued for that provider.</p>


